#include <iostream>
#include <string>

using namespace std;

class Boy
{
private:
	string beloved_girl;
public:
	string name;
	int age;
	void putGirl(string girl);
	friend void printGirl(Boy boy);
};

void Boy::putGirl(string girl)
{
	beloved_girl = girl;
}

//该函数是友元函数
void printGirl(Boy boy)
{
	cout << boy.beloved_girl << endl;
}

class Box
{
public:
	static int objectCount;
	// 构造函数定义
	Box(double l = 2.0, double b = 2.0, double h = 2.0)
	{
		cout << "Constructor called." << endl;
		length = l;
		breadth = b;
		height = h;
		// 每次创建对象时增加 1
		objectCount++;
	}
	double Volume()
	{
		return length * breadth * height;
	}
	static int getCount()
	{
		return objectCount;
	}
private:
	double length;     // 长度
	double breadth;    // 宽度
	double height;     // 高度
};

// 初始化类 Box 的静态成员
int Box::objectCount = 0;


